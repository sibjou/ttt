        <h1>Последние успехи в настольном теннисе в России</h1>
        <img src="../images/table-tennis-russia.jpg" alt="Настольный теннис в России">
        <p>Россия — одна из стран, которая стремительно набирает обороты в развитии настольного тенниса. На протяжении последних лет российские спортсмены стали показывать впечатляющие результаты на международных соревнованиях.</p>
        
        <h2>Выдающиеся игроки</h2>
        <p>К числу самых ярких звезд в российском настольном теннисе относится **Кирилл Скачков**. Он неоднократно представлял Россию на чемпионатах мира и Европы, демонстрируя высокий класс игры. Еще одним ярким именем является **Полина Михайлова**, которая славится своим мастерством в защитной игре.</p>
        
        <h2>Медали на чемпионатах</h2>
        <p>На недавнем Чемпионате Европы, который проходил в Германии, сборная России завоевала две бронзовые медали в командных соревнованиях. Это событие стало важным этапом в продвижении российского настольного тенниса на мировой арене.</p>
        
        <h2>Достижения молодежи</h2>
        <p>Особенно стоит отметить успехи молодежи. Российские юниоры активно выступают на международных турнирах ITTF, где завоевывают медали. Это говорит о качественной работе тренеров и федерации, которые готовят молодое поколение к будущим успехам.</p>
        
        <h2>Инфраструктура и развитие</h2>
        <p>В последние годы в России были построены десятки новых теннисных центров, включая современный комплекс в Казани. Благодаря этому молодые игроки получают доступ к качественным условиям для тренировок.</p>
        
        <h2>Планы на будущее</h2>
        <p>Федерация настольного тенниса России активно сотрудничает с международными организациями, чтобы привлечь больше турниров в страну. В 2025 году Россия планирует провести крупный международный турнир с участием звезд мирового настольного тенниса.</p>
